layui.use(['form','jquery'], function () {
    var form = layui.form;
    var $ = layui.jquery;
    //点击全选, 勾选
    form.on('checkbox(oneAllChoose)', function (data) {
        var child = $(".oneAll input[type='checkbox']");
        child.each(function (index, item) {
            item.checked = data.elem.checked;
        });
        form.render('checkbox');
    });
});

